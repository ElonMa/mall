package com.may.mall.ware.service.impl;

import com.may.common.constant.WareConstant;
import com.may.mall.ware.service.WareSkuService;
import com.may.mall.ware.vo.MergeVo;
import com.may.mall.ware.entity.PurchaseDetailEntity;
import com.may.mall.ware.service.PurchaseDetailService;
import com.may.mall.ware.vo.PurchaseDoneVo;
import com.may.mall.ware.vo.PurchaseItemDoneVo;
import org.springframework.beans.factory.annotation.Autowired;
import org.springframework.stereotype.Service;

import java.util.ArrayList;
import java.util.Date;
import java.util.List;
import java.util.Map;
import java.util.stream.Collectors;

import com.baomidou.mybatisplus.core.conditions.query.QueryWrapper;
